-module(sentibot_app).

-behaviour(application).

% API
-export([]).

%% Application callbacks
-export([ start/2
        , stop/1]).

%%====================================================================
%% API
%%====================================================================



%%====================================================================
%% application callbacks
%%====================================================================

start(_StartType, _StartArgs) ->
  lager:start(),
  application:ensure_all_started(slacker),
  sentibot_sup:start_link().

stop(_State) ->
  ok.

